// exploring various ways of declaring and initialisation of variables

// declaring and initilising at once
var a = 5;
var b = 4;
var sum = a + b;

// declaring and later initialise
var a, b, sum;
a = 5;
b = 4;
sum = a + b;

// as a single line
var a = 5, b = 4, sum = a + b;